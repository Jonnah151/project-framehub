export function findAnswer(query: string): string {
  const lower = query.toLowerCase();
  let bestMatch: KnowledgeEntry | null = null;
  let bestScore = 0;

  for (const entry of knowledgeBase) {
    let score = 0;
    for (const keyword of entry.keywords) {
      if (lower.includes(keyword.toLowerCase())) {
        score += 1;
      }
    }
    if (score > bestScore) {
      bestScore = score;
      bestMatch = entry;
    }
  }

  if (bestMatch && bestScore > 0) {
    return bestMatch.answer;
  }

  return 'Samahani, sina taarifa kuhusu swali hilo. Unaweza kuuliza kuhusu: muungano wa Tanganyika na Zanzibar, bendera ya Tanzania, mikoa, viongozi, Kilimanjaro, Serengeti, Ujamaa na Kujitegemea, au mada nyingine zinazohusu Tanzania.';
}
